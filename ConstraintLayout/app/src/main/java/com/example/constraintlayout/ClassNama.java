package com.example.constraintlayout;

public class ClassNama {
    private String Nama;

    //inisialisasi objek
    public ClassNama(String nama)
    {
        this.Nama = nama;
    }
    //membuat method getName untuk mengembalikan nilai ke variabel nama
    public String getNama()
    {
        return this.Nama;
    }
}
